    class Solution {
        public void moveZeroes(int[] nums) {
            int j = 0;
            for (int i = 0; i < nums.length; i++) {
                if (nums[i] != 0) {
//                    swap(nums[i], nums[j]);
                    int temp = nums[i];
                    nums[i] = nums[j];
                    nums[j] = temp;
                    j ++;
                }

            }
        }

        private void swap(int a, int b) {
            if (a == b) return;
            int temp = b;
            b = a;
            a = temp;
        }
    }
